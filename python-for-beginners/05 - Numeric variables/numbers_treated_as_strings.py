# Python은 변수가 어떤 데이터 타입인지 추측해야합니다.

# input 함수가 문자열을 반환해야 하기 때문에
# 입력한 값은 문자열 타입으로 저장됩니다.
first_num = input('Enter first number ')
second_num = input('Enter second number ')

# first_num과 second_num이 문자열 타입이기 때문에 + 연산자를 사용하면
# first_name과 last_name을 연결했던 것과 같이 문자열이 연결되어 출력됩니다.
print(first_num + second_num)
