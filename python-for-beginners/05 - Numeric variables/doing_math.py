# 변수가 생성될 때 숫자 값이 할당되었기 때문에
# Python은 해당 변수를 숫자 타입으로 인식합니다.
first_num = 6
second_num = 2

# 숫자 변수로는 다양한 수학 연산들을 수행할 수 있습니다.
print('addition')
print(first_num + second_num)
print('subtraction')
print(first_num - second_num)
print('multiplication')
print(first_num * second_num)
print('division')
print(first_num / second_num)
print('exponent')
print(first_num ** second_num)
